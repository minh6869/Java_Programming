package QLCB;

/**
 *
 * @author Bugs
 */
public class Canbo {
    private String sotk;
    private String hoten;
    private String gt;
    private String diachi;
    private String luong;

    public Canbo() {
    }
    

    public Canbo(String sotk, String hoten, String gt, String diachi, String luong) {
        this.sotk = sotk;
        this.hoten = hoten;
        this.gt = gt;
        this.diachi = diachi;
        this.luong = luong;
    }

    public String getSotk() {
        return sotk;
    }

    public void setSotk(String sotk) {
        this.sotk = sotk;
    }

    public String getHoten() {
        return hoten;
    }

    public void setHoten(String hoten) {
        this.hoten = hoten;
    }

    public String getGt() {
        return gt;
    }

    public void setGt(String gt) {
        this.gt = gt;
    }

    public String getDiachi() {
        return diachi;
    }

    public void setDiachi(String diachi) {
        this.diachi = diachi;
    }

    public String getLuong() {
        return luong;
    }

    public void setLuong(String luong) {
        this.luong = luong;
    }
    
    
    
}
